async def long_context_lookup(
    *, 
    topic: str, 
    distilled: bool = True,  # CHANGED: Default to True for inline results
    query: str | None = None,  # NEW: Query string for context-aware extraction
    min_confidence: float = 0.5,  # NEW: Minimum confidence threshold (lowered for demo)
    **_: Any
) -> dict[str, Any]:
    """
    Returns KEY FACTS from climate policy document (~80 tokens).
    
    Phase 3: Uses smart extraction with validation and fallback.
    Set distilled=False only if you need the full verbose document (~3000 tokens).
    """
    # Import here to avoid circular dependency
    from aho.fact_extractor import extract_key_facts, QueryContext, FactExtractor
    from aho.fact_validator import FactValidator, InlineResultValidator
    
    # Always extract facts first (for metadata)
    extractor = FactExtractor()
    context = QueryContext(query=query or f"Find {topic} information", topic="climate")
    facts = extractor.extract_smart(_LONG_DOCUMENT, context, max_facts=5)
    
    # Phase 3: Validate extracted facts
    validator = FactValidator()
    validation = validator.validate(facts, domain="climate_policy")
    
    # Check if we should use inline or fallback to full document
    inline_validator = InlineResultValidator(min_confidence=min_confidence)
    
    if facts:
        facts_output = extractor.format_inline(facts)
        structured = extractor.format_structured(facts)
    else:
        facts_output = "KEY FACTS:\n• No specific facts extracted for this query\n• Document contains climate policy information"
        structured = {}
    
    # Validate the inline result
    result_check = inline_validator.validate_inline_result(
        inline_output=facts_output,
        facts=facts,
        domain="climate_policy",
        metadata={"token_estimate": len(facts_output.split())},
    )
    
    # Phase 3: Fallback to full document if validation fails
    if distilled and result_check["recommendation"] == "use_full_document":
        # Return full document with warning
        return {
            "success": True,
            "output": _LONG_DOCUMENT,
            "error": None,
            "metadata": {
                "source": "mock_policy_db",
                "topic": topic,
                "document_title": "Global Climate Policy Reference Document v4.2",
                "token_estimate": 3100,
                "distilled": False,
                "fallback_reason": "low_confidence",
                "validation": result_check,
                "note": "Fell back to full document due to low extraction confidence. Use distilled=False explicitly if you need the full document.",
            },
        }
    
    # Return inline results (either approved or user explicitly wants distilled)
    if not distilled:
        return {
            "success": True,
            "output": _LONG_DOCUMENT,
            "error": None,
            "metadata": {
                "source": "mock_policy_db",
                "topic": topic,
                "document_title": "Global Climate Policy Reference Document v4.2",
                "token_estimate": 3100,
                "distilled": False,
            },
        }
    
    # Return validated inline results
    return {
        "success": True,
        "output": facts_output,
        "error": None,
        "metadata": {
            "source": "mock_policy_db",
            "topic": topic,
            "document_title": "Global Climate Policy Reference Document v4.2",
            "token_estimate": len(facts_output.split()),  # Approximate
            "distilled": True,
            "extraction_topic": topic,
            "query": query,
            "extracted_facts": structured,
            "fact_count": len(facts),
            "validation": validation,
            "confidence": validation["confidence"],
            "passed_validation": result_check["approved"],
        },
    }


