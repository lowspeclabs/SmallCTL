"""
aho/mock_tools.py
-----------------
Mock tool handlers for the AHO execution sandbox.

These simulate real-world tool calls (weather, clothing, long-context documents)
so that the harness can run fully offline, making strategy evaluation fast and
deterministic.

The Harness is configured to inject these tools into its registry via a
custom build_registry call in harness_runner.py — the existing
src/smallctl/tools/register.py is not modified.

Context-pressure tools
----------------------
`long_context_lookup`  — returns a ~3 000-token reference document to simulate
                         retrieving a verbose policy/RAG chunk.
`summarize_report`     — returns a separate ~2 500-token analyst report, adding
                         a second large injection to the conversation history.

Used by the 'long_context_stress' task in harness_config.json to evaluate how
well a strategy handles content that pushes past the 8 K context limit.
"""

from __future__ import annotations

import random
from typing import Any

# ---------------------------------------------------------------------------
# Deterministic seed per (trial_id, call_index) for reproducibility
# ---------------------------------------------------------------------------

_WEATHER_OPTIONS = [
    {"condition": "rainy", "temperature_c": 12, "wind_kmh": 25},
    {"condition": "sunny", "temperature_c": 28, "wind_kmh": 8},
    {"condition": "snowy", "temperature_c": -3, "wind_kmh": 15},
    {"condition": "cloudy", "temperature_c": 18, "wind_kmh": 12},
    {"condition": "stormy", "temperature_c": 10, "wind_kmh": 60},
]

_CLOTHING_MAP: dict[str, list[str]] = {
    "rainy": ["umbrella", "raincoat", "waterproof boots"],
    "sunny": ["t-shirt", "sunscreen", "sunglasses"],
    "snowy": ["coat", "gloves", "thermal socks"],
    "cloudy": ["jacket", "jeans", "light sweater"],
    "stormy": ["heavy raincoat", "umbrella", "rubber boots"],
}


async def weather_lookup(*, city: str, trial_seed: int = 0, **_: Any) -> dict[str, Any]:
    """
    Returns mock weather for a city.
    trial_seed is injected by harness_runner to make trials independently random
    but reproducible for a given seed.
    """
    rng = random.Random(trial_seed)
    weather = rng.choice(_WEATHER_OPTIONS)
    return {
        "success": True,
        "output": {
            "city": city,
            "condition": weather["condition"],
            "temperature_c": weather["temperature_c"],
            "wind_kmh": weather["wind_kmh"],
        },
        "error": None,
        "metadata": {"source": "mock", "trial_seed": trial_seed},
    }


async def clothing_suggest(*, condition: str, **_: Any) -> dict[str, Any]:
    """
    Returns a clothing suggestion based on weather condition.
    The ground_truth checker looks for any of these keywords in the final answer.
    """
    options = _CLOTHING_MAP.get(condition.lower(), ["a comfortable outfit"])
    suggestion = options[0]
    return {
        "success": True,
        "output": {
            "condition": condition,
            "suggestion": suggestion,
            "alternatives": options[1:],
        },
        "error": None,
        "metadata": {"source": "mock"},
    }


